variable = 'Hello World!'
print(variable)
#var length
print(len(variable))
#var uppercase
print(variable.upper())
#var lowercase
print(variable.lower())
#count how many letters are used in the string
print(variable.count('l'))
#find char in string (returns position)
print(variable.find('!'))
#replace char
print(variable.replace('!', '?'))
